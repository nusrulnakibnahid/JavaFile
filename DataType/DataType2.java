package DataType;

public class DataType2 {
    public static void main(String[] args){

        boolean b = false;
        char c = 'A';
        short s = 32655;
        int i = 20;
        float f = 20.45f;
        double d = 39.980;
    
        System.out.println("b = "+b);
    
        System.out.println("c = "+c);
    
        System.out.println("i = "+i);
    
        System.out.println("s = "+s);
    
        System.out.println("f = "+f);
    
        System.out.println("d = "+d);
    
      }
    
}
