package BasicJava;

public class PreIncrementDemo {
    public static void main(String[] args) {
        int y = 30;
        int x;

        x = ++y; //Prefix Increment (Here output show x=26)
        System.out.println("x  = " +x);

        x = y;
        System.out.print("Prefix Increment(x)="+x); 

    }
}
