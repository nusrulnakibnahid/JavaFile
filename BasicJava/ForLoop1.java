package BasicJava;
//print sum of all the numbers from 1-10(using for loop)
public class ForLoop1 {
    public static void main(String[] args) {
        int sum = 0;

        for(int i = 1; i<= 10; i++) {

            sum = sum + i;

        }
         System.out.print("Sum of all numbers from 1-10: " +sum);
         
         }
        
    }

