package Control_Statement.Looping;

public class WhileLoopDemo {
    public static void main(String[] args){

        int i = 1;
        while(i<=50){
            System.out.println(i+" Hi Coder ");
            i++;
        }

        int j =1; //odd number
        while(j<=50){
            System.out.println(j);
            j =j+2;
        }

        int k =0;
        while(k<=50){ //even number
            System.out.println(k);
            k =k+2;
        }


    }
}
