package Practice;

public class ForLoopPractice {
    public static void main(String[] args) {
        
        //addition
        int a,sum=0;
        for(a=1;a<=10;a++){
        sum=sum+a;
        }
            System.out.println("Addition(1-10):"+sum);


        //Subtraction
        int b,sub=0;
        for(b=1;b<=20;b++){
            sub = sub - b;
        }    
        System.out.println("Subtraction(1-20):"+sub);


    }
}
