package Practice;

public class UnaryPlusMinus {
    public static void main(String[] args) {
        
        int x = -10;
        int result;

        result = +x;
        System.out.println("Result in unary Plus: "+result);

        result = -x;
        System.out.print("Result in unary Minus: "+result);
        result = -x; 
    }
}
